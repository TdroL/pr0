string getBasicInfo(string prefix)
{
	ostringstream oss;

	oss << prefix << "Version   \tOpenGL " << glGetString(GL_VERSION) << endl;
	oss << prefix << "Vendor    \t" << glGetString(GL_VENDOR) << endl;
	oss << prefix << "Renderer  \t" << glGetString(GL_RENDERER);

	return oss.str();
}

string getExtensionsInfo(string prefix)
{
	ostringstream oss;

	GLuint num;
	gl::get(GL_NUM_EXTENSIONS, num);

	for (size_t i : range(num))
	{
		oss << prefix << glGetStringi(GL_EXTENSIONS, i) << endl;
	}

	return oss.str();
}

