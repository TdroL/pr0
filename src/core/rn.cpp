string getBasicInfo()
{
	ostringstream oss;

	oss << "Version   \tOpenGL " << glGetString(GL_VERSION) << endl;
	oss << "Vendor    \t" << glGetString(GL_VENDOR) << endl;
	oss << "Renderer  \t" << glGetString(GL_RENDERER);

	return oss.str();
}

string getExtensionsInfo()
{
	ostringstream oss;

	GLuint num;
	rn::get(GL_NUM_EXTENSIONS, num);

	for (GLuint i = 0; i < num; i++)
	{
		oss << glGetStringi(GL_EXTENSIONS, i) << endl;
	}

	return oss.str();
}

